# ── Brief assembly ──────────────────────────────────────────────────────────

def build_brief(community_id: str, *, use_llm: bool, verbose: bool = False) -> dict:
    record = get_community_record(community_id)
    if verbose:
        print(f"  ▸ {record['name']:25s} ({community_id})")

    rd = resource.get_resource_data(community_id)
    sizing = design.size_system(community_id)
    econ = economics.compute_economics(community_id, sizing.model_dump())

    validation = build_validation(record)
    econ_dict = econ.model_dump()

    if use_llm:
        funding = llm_grant_finder(record, econ_dict)
        brief_md = llm_brief_writer(record, rd, sizing, econ, funding, validation)
    else:
        funding = fallback_grant_finder(record, econ_dict)
        brief_md = fallback_brief_writer(record, rd, sizing, econ, funding, validation)

    capital_point = econ.capital_cost_cad.point
    ranking_inputs = RankingInputs(
        dollar_per_dollar=round(econ.annual_cost_saved_cad / capital_point, 6),
        co2_per_dollar_kg=round((econ.annual_co2_avoided_tonnes * 1000) / capital_point, 6),
        equity_multiplier=(
            EQUITY_MULTIPLIER_INDIGENOUS if record.get("indigenous") else EQUITY_MULTIPLIER_DEFAULT
        ),
    )

    return {
        "id": record["id"],
        "name": record["name"],
        "lat": record["lat"],
        "lon": record["lon"],
        "depth": record.get("depth", "light"),
        "region": record["region"],
        "indigenous": bool(record.get("indigenous")),
        "resource": rd.model_dump(),
        "system": sizing.model_dump(),
        "economics": econ.model_dump(),
        "funding": funding.model_dump(),
        "validation": validation.model_dump(),
        "ranking_inputs": ranking_inputs.model_dump(),
        "brief_markdown": brief_md,
    }


# ── CLI ─────────────────────────────────────────────────────────────────────

def main() -> int:
    parser = argparse.ArgumentParser(
        description=(
            "Pre-compute Meridian briefs for the 20 NL diesel-dependent "
            "communities. Default mode calls Orchestrate; --no-llm uses the "
            "deterministic Python fallback."
        )
    )
    parser.add_argument(
        "--no-llm",
        action="store_true",
        help="Use deterministic fallback for Grant Finder and Brief Writer (no LLM, no network).",
    )
    parser.add_argument("--verbose", "-v", action="store_true", help="Per-community progress.")
    parser.add_argument("--only", help="Process only this community id.")
    parser.add_argument(
        "--output",
        default=str(BRIEFS_PATH_DEFAULT),
        help=f"Output path (default: {BRIEFS_PATH_DEFAULT.relative_to(ROOT)})",
    )
    args = parser.parse_args()

    use_llm = not args.no_llm
    community_ids = [args.only] if args.only else list_community_ids()

    mode = "LLM (Orchestrate)" if use_llm else "fallback (deterministic)"
    word = "community" if len(community_ids) == 1 else "communities"
    print(f"Pre-computing briefs for {len(community_ids)} {word} — {mode} mode\n")

    briefs = []
    for cid in community_ids:
        try:
            briefs.append(build_brief(cid, use_llm=use_llm, verbose=args.verbose))
        except NotImplementedError as e:
            print(f"\n✗ {cid}: {e}", file=sys.stderr)
            return 2
        except Exception as e:
            print(f"\n✗ {cid}: {type(e).__name__}: {e}", file=sys.stderr)
            return 1

    output = {
        "metadata": {
            "schema_version": "1.0",
            "generation_mode": "llm" if use_llm else "fallback",
            "communities_total": len(briefs),
        },
        "communities": briefs,
    }

    out_path = Path(args.output)
    out_path.write_text(json.dumps(output, indent=2))
    print(f"\nWrote {len(briefs)} briefs → {out_path.relative_to(ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
