# ── Fallback Grant Finder ───────────────────────────────────────────────────

def _is_labrador(region: str) -> bool:
    r = region.lower()
    return "labrador" in r or "nunatsiavut" in r


def fallback_grant_finder(community: dict, econ_dict: dict) -> FundingMatch:
    """Rule-based eligibility — mirrors the hard rules in grant_finder.md."""
    is_indigenous = bool(community.get("indigenous"))
    is_labrador = _is_labrador(community["region"])
    capital_point = int(econ_dict["capital_cost_cad"]["point"])

    eligible: list[EligibleProgram] = []

    if is_labrador and is_indigenous:
        eligible.append(
            EligibleProgram(
                name="Reducing Diesel Dependency in Isolated Labrador Communities",
                max_cad=min(25_000_000, capital_point),
                eligibility_reasoning=(
                    "Indigenous-governed community in Labrador; eligible under "
                    "the program's geographic and Indigenous-priority criteria."
                ),
            )
        )

    if is_indigenous:
        eligible.append(
            EligibleProgram(
                name="Indigenous Off-Diesel Initiative",
                max_cad=min(15_000_000, capital_point),
                eligibility_reasoning=(
                    "Indigenous off-grid community pursuing community-led "
                    "clean energy; aligns with IODI scope."
                ),
            )
        )

    eligible.append(
        EligibleProgram(
            name="Clean Energy for Rural and Remote Communities",
            max_cad=min(15_000_000, capital_point),
            eligibility_reasoning=(
                "Off-grid diesel-dependent community; eligible under CERRC's "
                "national rural/remote scope."
            ),
        )
    )

    if capital_point >= 5_000_000:
        eligible.append(
            EligibleProgram(
                name="Smart Renewables and Electrification Pathways",
                max_cad=min(20_000_000, capital_point // 2),
                eligibility_reasoning=(
                    "Capital cost exceeds $5M; project scale qualifies under "
                    "SREP utility-scale criteria, especially as part of a "
                    "regional procurement bundle."
                ),
            )
        )

    potential_coverage = min(sum(p.max_cad for p in eligible), capital_point)

    return FundingMatch(
        eligible_programs=eligible,
        potential_coverage_cad=potential_coverage,
        notes=(
            "Generated by Python fallback rules. Replace with LLM Grant Finder "
            "once Orchestrate is wired."
        ),
    )


# ── Fallback Brief Writer ───────────────────────────────────────────────────

BRIEF_TEMPLATE = """## {name} — Off-Diesel Pre-Feasibility Brief

**Community:** {name}
**Region:** {region}
**Governance:** {governance}
**Current diesel load:** ~{load_kw:,} kW average, ~{annual_diesel_mil:.2f} M litres / year

## Executive Summary

A **{mix_label}** system is the lowest-cost path to reduce {name}'s diesel reliance. Estimated capital cost is **${cap_point_mil:.1f} M** (range ${cap_low_mil:.1f} M – ${cap_high_mil:.1f} M). The system would displace approximately **{fuel_saved_mil:.2f} million litres of diesel per year**, saving **${cost_saved_mil:.1f} M annually** and avoiding **{co2_t:,} tonnes of CO2**. Simple payback (excluding O&M): **{payback_yrs} years**. Federal funding from {primary_program} could cover up to **${primary_max_mil:.1f} M** of the capital cost.

## Resource Snapshot

Wind speed at 80 m: **{wind_mps} m/s** ({wind_quality} resource). Solar GHI: **{solar_ghi} kWh/m²/day** ({solar_quality} resource). Data sources: Global Wind Atlas (wind), NASA POWER (solar).

## Proposed System

| Component | Capacity range |
|---|---|
| Wind | {wind_kw_low:,} – {wind_kw_high:,} kW |
| Solar | {solar_kw_low:,} – {solar_kw_high:,} kW |
| Battery | {batt_kwh_low:,} – {batt_kwh_high:,} kWh |
| Retained diesel | {retained_diesel_kw:,} kW |

{sizing_rationale}

## Economics

| Metric | Value |
|---|---|
| Capital cost (low / point / high) | ${cap_low_mil:.1f} M / **${cap_point_mil:.1f} M** / ${cap_high_mil:.1f} M |
| Annual fuel saved | {fuel_saved_litres:,} litres |
| Annual cost saved | ${cost_saved_mil:.1f} M |
| Annual CO2 avoided | {co2_t:,} tonnes |
| Simple payback | {payback_yrs} years |

Simple payback excludes ongoing operations and maintenance. Real-world payback typically extends a few years longer once O&M is accounted for.

## Funding Match

{funding_block}

**Total indicative federal coverage: ${coverage_mil:.1f} M**

## Validation / Next Steps

{validation_text}
"""

FUNDING_LINE = "- **{name}** — indicative coverage up to **${max_mil:.1f} M**. {reason}"


def _quality(wind_mps: float, solar_ghi: float) -> tuple[str, str]:
    wq = "strong" if wind_mps >= 7 else "moderate" if wind_mps >= 5 else "weak"
    sq = "strong" if solar_ghi >= 4 else "moderate" if solar_ghi >= 3.5 else "weak"
    return wq, sq


def fallback_brief_writer(
    community: dict,
    rd,
    sizing,
    econ,
    funding: FundingMatch,
    validation: ValidationInfo,
) -> str:
    primary = funding.eligible_programs[0] if funding.eligible_programs else None
    funding_lines = [
        FUNDING_LINE.format(
            name=p.name,
            max_mil=p.max_cad / 1e6,
            reason=p.eligibility_reasoning,
        )
        for p in funding.eligible_programs
    ]
    funding_block = "\n".join(funding_lines) if funding_lines else "_No eligible programs matched._"

    if validation.real_project_exists:
        validation_text = (
            f"{validation.real_project_summary} "
            f"Meridian's recommendation matches the qualitative technology mix "
            f"of the project currently under construction."
        )
    else:
        validation_text = (
            "Preliminary recommendation. Requires confirmation against the "
            "community's actual load profile, site survey data, and "
            "consultation with the relevant governance body before any "
            "design or procurement step."
        )

    wq, sq = _quality(rd.wind_speed_80m_mps, rd.solar_ghi_kwh_m2_day)

    return BRIEF_TEMPLATE.format(
        name=community["name"],
        region=community["region"],
        governance=community["governance"],
        load_kw=community["current_diesel_kw_avg"],
        annual_diesel_mil=community["annual_diesel_litres"] / 1e6,
        mix_label=sizing.mix_label,
        cap_point_mil=econ.capital_cost_cad.point / 1e6,
        cap_low_mil=econ.capital_cost_cad.low / 1e6,
        cap_high_mil=econ.capital_cost_cad.high / 1e6,
        fuel_saved_mil=econ.annual_fuel_saved_litres / 1e6,
        fuel_saved_litres=econ.annual_fuel_saved_litres,
        cost_saved_mil=econ.annual_cost_saved_cad / 1e6,
        co2_t=econ.annual_co2_avoided_tonnes,
        payback_yrs=econ.payback_years,
        primary_program=primary.name if primary else "available programs",
        primary_max_mil=(primary.max_cad / 1e6) if primary else 0,
        wind_mps=rd.wind_speed_80m_mps,
        solar_ghi=rd.solar_ghi_kwh_m2_day,
        wind_quality=wq,
        solar_quality=sq,
        wind_kw_low=sizing.wind_kw.low,
        wind_kw_high=sizing.wind_kw.high,
        solar_kw_low=sizing.solar_kw.low,
        solar_kw_high=sizing.solar_kw.high,
        batt_kwh_low=sizing.battery_kwh.low,
        batt_kwh_high=sizing.battery_kwh.high,
        retained_diesel_kw=sizing.retained_diesel_kw,
        sizing_rationale=sizing.sizing_rationale,
        funding_block=funding_block,
        coverage_mil=funding.potential_coverage_cad / 1e6,
        validation_text=validation_text,
    )


# ── Validation block ────────────────────────────────────────────────────────

def build_validation(community: dict) -> ValidationInfo:
    status = (community.get("real_project_status") or "").strip()
    has_real_project = bool(status) and "no active" not in status.lower() and "none" not in status.lower()
    return ValidationInfo(
        real_project_exists=has_real_project,
        qualitative_match=has_real_project,
        real_project_summary=status or "No active off-diesel project announced.",
    )


