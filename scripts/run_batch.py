"""
Batch pre-compute — walk the 20 communities, build a full per-community
brief for each, and write data/briefs.json. This file is the single source
of truth the React frontend reads via /briefs.

Two modes:

  * default (LLM-backed): the three deterministic Python tools run
    in-process, then Grant Finder and Brief Writer are called over HTTPS
    against watsonx Orchestrate. Requires .env credentials and the seven
    agents registered in Orchestrate. (LLM mode is still stubbed — see
    NotImplementedError below — wire to Orchestrate's REST URL pattern
    once your instance is configured.)

  * --no-llm: deterministic Python fallback. Grant Finder uses rule-based
    eligibility matching; Brief Writer uses a string template that
    substitutes the computed values. Zero LLM calls, zero network. This
    is what unblocks the frontend build before Orchestrate is wired, AND
    serves as the safety-net path if Orchestrate is flaky on demo morning.

/* USAGE:
    # Deterministic fallback batch (no LLM, no network):
    python scripts/run_batch.py --no-llm

    # LLM-backed batch (once Orchestrate is wired):
    python scripts/run_batch.py

    # Limit to one community for debugging:
    python scripts/run_batch.py --only nain --no-llm --verbose

    # Custom output path:
    python scripts/run_batch.py --no-llm --output data/briefs_test.json
*/
"""

